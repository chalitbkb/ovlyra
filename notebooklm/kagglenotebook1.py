        "### 🔍 ส่วนที่ 3: อ่าน Parquet + Pre-filter ด้วย Byte-size Estimate\n",
        "ประเมิน duration จากขนาด MP3 bytes (ไม่ต้อง spawn subprocess = **เร็วมาก**)\n",
