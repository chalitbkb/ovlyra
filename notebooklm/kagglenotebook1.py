        "### ⚡ ส่วนที่ 4: แปลง MP3 → WAV ด้วย Parallel FFmpeg + Duration แม่นยำ\n",
        "แปลงเฉพาะไฟล์ที่ผ่าน pre-filter แล้ว + ตรวจ duration จริงหลังแปลง"
