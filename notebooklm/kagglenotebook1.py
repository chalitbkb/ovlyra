        "import pyarrow.parquet as pq\n",
        "\n",
        "os.makedirs(OUTPUT_WAV_DIR, exist_ok=True)\n",
        "\n",
        "# MP3 bitrate constants (bits per second)\n",
        "MP3_MIN_BITRATE = 64_000    # 64 kbps (lowest common)\n",
        "MP3_MAX_BITRATE = 320_000   # 320 kbps (highest common)\n",
        "\n",
        "def pre_filter_by_size(audio_bytes, min_sec, max_sec):\n",
        "    \"\"\"กรองจากขนาด bytes ของ MP3 (เร็วมาก ไม่ต้อง subprocess)\n",
        "    \n",
        "    Returns:\n",
        "        'ok'    = น่าจะผ่าน ให้แปลงต่อ\n",
        "        'short' = สั้นเกินแน่นอน (แม้ bitrate ต่ำสุดก็ยังสั้น)\n",
        "        'long'  = ยาวเกินแน่นอน (แม้ bitrate สูงสุดก็ยังยาว)\n",
        "    \"\"\"\n",
        "    size_bits = len(audio_bytes) * 8\n",
        "    \n",
        "    # ถ้าแม้ที่ bitrate ต่ำสุด ยังสั้นกว่า min_sec → สั้นแน่นอน\n",
        "    max_possible_duration = size_bits / MP3_MIN_BITRATE\n",
        "    if max_possible_duration < min_sec:\n",
        "        return 'short'\n",
        "    \n",
        "    # ถ้าแม้ที่ bitrate สูงสุด ยังยาวกว่า max_sec → ยาวแน่นอน\n",
        "    min_possible_duration = size_bits / MP3_MAX_BITRATE\n",
        "    if min_possible_duration > max_sec:\n",
        "        return 'long'\n",
        "    \n",
        "    return 'ok'\n",
        "\n",
        "\n",
        "print(\"📖 กำลังอ่าน Parquet files + กรอง...\")\n",
        "all_rows = []\n",
        "skipped_short = 0\n",
        "skipped_long = 0\n",
        "skipped_invalid = 0\n",
        "\n",
        "parquet_files = sorted(\n",
        "    [os.path.join(PARQUET_DIR, f) for f in os.listdir(PARQUET_DIR) if f.endswith(\".parquet\")],\n",
        "    key=lambda x: int(os.path.basename(x).split(\".\")[0])\n",
        ")\n",
        "\n",
        "total_collected = 0\n",
        "total_scanned = 0\n",
        "for pf in parquet_files:\n",
        "    if MAX_CLIPS and total_collected >= MAX_CLIPS:\n",
        "        break\n",
        "    \n",
        "    table = pq.read_table(pf)\n",
        "    df_chunk = table.to_pandas()\n",
        "    \n",
        "    for idx, row in df_chunk.iterrows():\n",
        "        if MAX_CLIPS and total_collected >= MAX_CLIPS:\n",
        "            break\n",
        "        \n",
        "        total_scanned += 1\n",
        "        \n",
        "        audio_data = row['audio']\n",
        "        sentence = str(row['sentence']).strip()\n",
        "        utterance = str(row['utterance']).strip()\n",
        "        \n",
        "        if not sentence or not audio_data:\n",
        "            skipped_invalid += 1\n",
        "            continue\n",
        "        \n",
        "        if isinstance(audio_data, dict):\n",
        "            audio_bytes = audio_data.get('bytes', b'')\n",
        "        elif isinstance(audio_data, bytes):\n",
        "            audio_bytes = audio_data\n",
        "        else:\n",
        "            skipped_invalid += 1\n",
        "            continue\n",
        "        \n",
        "        if not audio_bytes or len(audio_bytes) < 100:\n",
        "            skipped_invalid += 1\n",
        "            continue\n",
        "        \n",
        "        # ====== PRE-FILTER: ประเมินจากขนาด bytes (เร็วมาก!) ======\n",
        "        verdict = pre_filter_by_size(audio_bytes, MIN_DURATION_SEC, MAX_DURATION_SEC)\n",
        "        if verdict == 'short':\n",
        "            skipped_short += 1\n",
        "            continue\n",
        "        if verdict == 'long':\n",
        "            skipped_long += 1\n",
        "            continue\n",
        "        # ==========================================================\n",
        "        \n",
        "        base_name = utterance.replace('.mp3', '').replace('.wav', '')\n",
        "        \n",
        "        all_rows.append({\n",
        "            'audio_bytes': audio_bytes,\n",
        "            'sentence': sentence,\n",
        "            'base_name': base_name,\n",
        "        })\n",
        "        total_collected += 1\n",
        "    \n",
        "    del df_chunk, table\n",
        "    \n",
        "    print(f\"  📄 {os.path.basename(pf)}: สแกน {total_scanned} | ผ่าน {total_collected} \"\n",
        "          f\"| ข้ามสั้น {skipped_short} | ข้ามยาว {skipped_long} | invalid {skipped_invalid}\")\n",
        "\n",
        "print(f\"\\n✅ Pre-filter เสร็จ: {total_collected} ผ่าน จาก {total_scanned} ที่สแกน\")\n",
        "print(f\"   ❌ สั้นเกิน (แม้ bitrate 64kbps ก็ <{MIN_DURATION_SEC}s): {skipped_short}\")\n",
        "print(f\"   ❌ ยาวเกิน (แม้ bitrate 320kbps ก็ >{MAX_DURATION_SEC}s): {skipped_long}\")\n",
        "print(f\"   ❌ ข้อมูลไม่ถูกต้อง: {skipped_invalid}\")\n",
